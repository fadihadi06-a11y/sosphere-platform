export function useT(lang: Lang) {
  return useCallback((key: string): string => {
    return S[lang]?.[key] || S.en[key] || key;
  }, [lang]);
}

// ── Language Picker Component ─────────────────────────────────
export function LanguagePicker({ lang, onChange, compact }: { lang: Lang; onChange: (l: Lang) => void; compact?: boolean }) {
  const [open, setOpen] = useState(false);
  const meta = LANG_META[lang];
  return (
    <div className="relative">
      <button onClick={() => setOpen(!open)}
        className="flex items-center gap-1.5 px-2.5 py-1.5 rounded-lg"
        style={{ background: "rgba(255,255,255,0.03)", border: "1px solid rgba(255,255,255,0.06)", fontSize: 10 }}>
        <svg className="size-3.5" viewBox="0 0 24 24" fill="none" stroke="rgba(255,255,255,0.45)" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
          <circle cx="12" cy="12" r="10"/><path d="M2 12h20"/><path d="M12 2a15.3 15.3 0 0 1 4 10 15.3 15.3 0 0 1-4 10 15.3 15.3 0 0 1-4-10 15.3 15.3 0 0 1 4-10z"/>
        </svg>
        <span>{meta.flag}</span>
        {!compact && <span style={{ color: "rgba(255,255,255,0.5)", fontWeight: 500 }}>{meta.native}</span>}
      </button>
      {open && (
        <>
          <div className="fixed inset-0 z-50" onClick={() => setOpen(false)} />
          <div className="absolute right-0 top-full mt-1 z-50 rounded-xl overflow-hidden overflow-y-auto"
            style={{
              width: 180, maxHeight: 280,
              background: "#0A1220", border: "1px solid rgba(255,255,255,0.08)",
              boxShadow: "0 8px 32px rgba(0,0,0,0.5)",
              scrollbarWidth: "none",
            }}>
            {(Object.keys(LANG_META) as Lang[]).map(l => (
              <button key={l} onClick={() => { onChange(l); setOpen(false); }}
                className="w-full flex items-center gap-2 px-3 py-2 text-left"
                style={{
                  background: l === lang ? "rgba(0,200,224,0.08)" : "transparent",
                  borderBottom: "1px solid rgba(255,255,255,0.03)",
                }}>
                <span style={{ fontSize: 14 }}>{LANG_META[l].flag}</span>
                <div className="flex-1 min-w-0">
                  <p style={{ fontSize: 11, fontWeight: l === lang ? 600 : 400, color: l === lang ? "#00C8E0" : "rgba(255,255,255,0.6)" }}>
                    {LANG_META[l].native}
                  </p>
                  <p style={{ fontSize: 8, color: "rgba(255,255,255,0.2)" }}>{LANG_META[l].label}</p>
                </div>
                {l === lang && <div className="size-1.5 rounded-full" style={{ background: "#00C8E0" }} />}
              </button>
            ))}
          </div>
        </>
      )}
    </div>
  );
}